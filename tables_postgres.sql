CREATE TABLE T_DEPT
(
  ID           SERIAL,
  NAME         VARCHAR(100) NOT NULL,
  DESCRIPTION  VARCHAR(200) NULL,
  PRIMARY KEY (ID)
);

CREATE TABLE T_EMP
(
  ID           SERIAL,
  NAME         VARCHAR(50)  NOT NULL,
  ADDRESS      VARCHAR(100) NOT NULL,
  AGE          INT          NOT NULL,
  DEPARTMENT   INT          NOT NULL,
  SALARY       INT,
  TEL          VARCHAR(20)  NOT NULL,
  PRIMARY KEY (ID),
  FOREIGN KEY (DEPARTMENT)
  REFERENCES T_DEPT (ID)
);

CREATE TABLE T_JOB
(
  ID             SERIAL,
  JOBNAME        VARCHAR(64)  NOT NULL,
  DESCRIPTION    VARCHAR(255) NULL,
  CRON           VARCHAR(64)  NOT NULL,
  SPRINGBEANNAME VARCHAR(64)  NOT NULL,
  METHODNAME     VARCHAR(64)  NOT NULL,
  CREATETIME     TIMESTAMP    NOT NULL,
  UPDATETIME     TIMESTAMP    NOT NULL DEFAULT NOW(),
  STATUS         SMALLINT     NOT NULL DEFAULT 1,
  ISSYSJOB       BOOL         NOT NULL DEFAULT FALSE,
  PRIMARY KEY (ID),
  UNIQUE (JOBNAME)
);

CREATE TABLE T_SYS_LOG
(
  ID             SERIAL,
  USERNAME       VARCHAR(50)   NULL,
  OPERATION      VARCHAR(100)  NULL,
  COST_TIME      INT,
  METHOD         VARCHAR(200)  NULL,
  PARAMS         VARCHAR(500)  NULL,
  IP             VARCHAR(64)   NULL,
  CREATE_TIME    TIMESTAMP     NOT NULL DEFAULT NOW(),
  PRIMARY KEY (ID)
);

CREATE TABLE T_TOKEN
(
  ID              VARCHAR(36)  NOT NULL,
  VAL             TEXT         NOT NULL,
  EXPIRETIME      TIMESTAMP    NOT NULL,
  CREATETIME      TIMESTAMP    NOT NULL,
  UPDATETIME      TIMESTAMP    NOT NULL,
  PRIMARY KEY (ID)
);

CREATE TABLE T_MAIL
(
  ID              SERIAL,
  USERID          INT          NOT NULL,
  SUBJECT         VARCHAR(255) NOT NULL,
  CONTENT         TEXT         NOT NULL,
  CREATETIME      TIMESTAMP    NOT NULL,
  UPDATETIME      TIMESTAMP    NOT NULL,
  PRIMARY KEY (ID)
);

CREATE TABLE T_MAIL_TO
(
  ID              SERIAL,
  MAILID          INT           NOT NULL,
  TOUSER          VARCHAR(128)  NOT NULL,
  STATUS          BOOL          NOT NULL DEFAULT TRUE,
  PRIMARY KEY (ID)
);

CREATE TABLE T_FILE_INFO
(
  ID               SERIAL,
  NAME             VARCHAR(128)  NOT NULL,
  URL              VARCHAR(1024) NOT NULL,
  SIZE             VARCHAR(100)  NOT NULL,
  TYPE             VARCHAR(100)  NOT NULL,
  CREATETIME       TIMESTAMP     NOT NULL,
  UPDATETIME       TIMESTAMP     NOT NULL,
  PRIMARY KEY (ID)
);

CREATE TABLE persistent_logins
(
  username character varying(64) NOT NULL,
  series character varying(64) NOT NULL,
  token character varying(64) NOT NULL,
  last_used timestamp without time zone NOT NULL,
  CONSTRAINT persistent_logins_pkey PRIMARY KEY (series)
);

